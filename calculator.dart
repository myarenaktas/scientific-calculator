import 'dart:math';

void main() {
  print("Scientific Calculator");

  double a = 25;
  double b = 5;

  print("Addition: ${a + b}");
  print("Subtraction: ${a - b}");
  print("Multiplication: ${a * b}");
  print("Division: ${a / b}");
  print("Power: ${pow(a, 2)}");
  print("Square Root: ${sqrt(a)}");
  print("Sin: ${sin(a)}");
  print("Cos: ${cos(a)}");
  print("Tan: ${tan(a)}");
  print("Log: ${log(a)}");
}